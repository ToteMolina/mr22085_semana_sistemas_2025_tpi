                <div class="tema">
                    <h3>Ejemplo Práctico: Obteniendo Información a Pie (Farmacia)</h3>
                    <p>Se desarrolló un caso práctico real de análisis de datos para una farmacia:</p>
                    
                    <h4>Variables Definidas:</h4>
                    <p>• Fecha de venta<br>
                    • Producto vendido<br>
                    • Precio unitario<br>
                    • Unidades vendidas<br>
                    • Stock disponible</p>
                    
                    <h4>Proceso de Análisis:</h4>
                    <p><strong>1. Captura:</strong> Registro simple de datos en planilla<br>
                    <strong>2. Limpieza:</strong> Depuración de datos inconsistentes o erróneos<br>
                    <strong>3. Métricas:</strong> Cálculo de ingresos totales y rotación de inventario</p>
                    
                    <h4>Indicadores Obtenidos:</h4>
                    <p>• Ticket promedio de compra<br>
                    • Top 10 productos más vendidos<br>
                    • Quiebres de stock (productos agotados)</p>
                    
                    <p><strong>Dataset utilizado:</strong> Documento con 10,000 registros de transacciones reales que fue procesado y filtrado durante la demostración en vivo.</p>
                    <div class="fotografia">
                        <img src="../public/img/dia2_sds25_taller1_4.png" alt="Datos">
