                    <p>Se exploraron ejemplos cotidianos donde los datos influyen en nuestras decisiones: desde elegir la ruta más rápida al trabajo usando apps de navegación, hasta decisiones de compra basadas en reseñas y calificaciones en línea.</p>
                </div>

