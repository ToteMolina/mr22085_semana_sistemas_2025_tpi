                    <p><strong>Data Science:</strong> 12-18 meses con base sólida en estadística y desarrollo de proyectos prácticos.</p>
                    <p><strong>Machine Learning / IA:</strong> 18-24 meses incluyendo matemática aplicada (álgebra lineal, cálculo) y práctica intensiva.</p>
                    <p><strong>DevOps / Ciberseguridad:</strong> 12 meses con enfoque hands-on y certificaciones clave en la especialidad.</p>
