                <h2>Comelona de Pupusas</h2>

                <div class="tema">
                    <p>
                        Además de la feria de logros, el día continuó con su siguiente actividad: La Comelona de
                        Pupusas,
                        una competencia que llenó el ambiente de risas, emoción e intriga. Estudiantes y Docentes se
                        unieron
                        en un reto simple, pero nada fácil: Comer la mayor cantidad de pupusas durante 15 minutos.
                    </p>
                    <p>
                        11 participantes se animaron a unirse a la competencia. Iniciaron con 5 pupusas cada uno y, al
                        terminarlas,
                        se iban agregando pupusas de una en una a las cuentas de cada uno. Finalmente se acabó el tiempo
                        y se nombraron
                        al primer y segundo lugar.
                    </p>
                    <p>
                        El segundo lugar con 13 pupusas fue <strong>Manuel Meléndez</strong> y el primer lugar con 16
                        pupusas fue
                        <strong>Ing. Guandique</strong>.
                    </p>
